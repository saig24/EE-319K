void SysTick_Init(void){
	NVIC_ST_CTRL_R = 0;
	NVIC_ST_RELOAD_R = 3000000;
	NVIC_ST_CURRENT_R = 0;
	NVIC_ST_CTRL_R |= 0x07;
}

void SysTick_Handler(void){
	ADCMail = ADC_In();
	if((ADCMail/100 == ADCLast/100))
		flag = 0;
	else
	{
		flag = 1;
		ADCLast = ADCMail;
	}
	for(int i = 0; i < 4; i++)
	{
		if(LaserTrack[i].Active == 1)
			LaserTrack[i].Y -= 4;
	}
	if((GPIO_PORTB_DATA_R &= 0x03) > 0 && buttonWait == 0)
	{
		PF1 ^= 0x02;
		newLaser();
		buttonWait = 10;
	}
	else if(buttonWait != 0)
		buttonWait--;
}

