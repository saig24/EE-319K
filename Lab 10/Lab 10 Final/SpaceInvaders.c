// ***************** outScore ****************
// run by GameEngine to output Score   
// Inputs: none
// Outputs: none
void outScore(){
		for(int i = 0; i < 4; i++){
			tempScore = (score - temp);
			tempScore /= scoreVariable;
			outNumber(tempScore, XScore, YScore);
			XScore += 8;
			temp += (tempScore * scoreVariable);
			scoreVariable /= 10;	
		}
		XScore = 96;
		scoreVariable = 1000;
		temp = 0;
	}
