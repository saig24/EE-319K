// ***************** PortD_Init ****************
// Activate Port D
// Inputs:  none
// Outputs: none
void PortD_Init(void){
  GPIO_PORTD_AMSEL_R = 0x00;    
  GPIO_PORTD_AFSEL_R = 0x00;        
	GPIO_PORTD_DIR_R &= !0x03;
	GPIO_PORTD_DEN_R |= 0x03;	
	startScreen();
	GPIO_PORTD_IS_R &= !0x03;
	GPIO_PORTD_IBE_R &= !0x03;
	GPIO_PORTD_IEV_R = 0x03;
	GPIO_PORTD_ICR_R = 0x01;
	GPIO_PORTD_IM_R |= 0x03;
	NVIC_PRI0_R = (NVIC_PRI0_R & 0x00FFFFF) | 0xA0000000;
	NVIC_EN0_R = 0x08;	
}

// ***************** Convert ****************
// convert slider pot values to pixel coordinates
// Inputs:  slider pot return value from PE2
