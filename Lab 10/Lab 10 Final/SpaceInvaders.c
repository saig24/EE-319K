// ***************** hitCheck ****************
// run by GameEngine to check hitboxes 
// Inputs: none
// Outputs: none
void hitCheck(){
	for(int i =0; i < 12; i++){
		for(int j = 0; j < 6; j++){
			if((LaserTrack[i].X >= BugTrack[j].X - 2) && (LaserTrack[i].X <= BugTrack[j].X + 12) && 
				(LaserTrack[i].Y + 9 >= BugTrack[j].Y) && (LaserTrack[i].Y <= BugTrack[j].Y + 11) && (BugTrack[j].Active == 1)){
				eraseSprites(i,j);
				}
			if(BugTrack[j].Y > 175)
				BugTrack[j].Active = 0;
			if((BugTrack[j].Y >= 138) && ((Convert(ADCMail) < BugTrack[j].X + 15) && (Convert(ADCMail) + 21 > BugTrack[j].X))  
				&& (BugTrack[j].Active == 1))
					endScreen();
			}
		}
		if(BugTrack[0].Active == 0 && BugTrack[1].Active == 0 && BugTrack[2].Active == 0 && BugTrack[3].Active == 0 && BugTrack[4].Active == 0
			&& BugTrack[5].Active == 0)
			gameOver();
	}

